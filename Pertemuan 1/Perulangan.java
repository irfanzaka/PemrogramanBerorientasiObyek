public class Perulangan {
    public static void main (String[]args){
        
        for(int i=1; i<=20; i+=2){
            System.out.print(" "+i);
        }
        int i=0;
        while( i <= 20){
            System.out.print("\n "+i);
            
            i+=2;
        }
    }
}
